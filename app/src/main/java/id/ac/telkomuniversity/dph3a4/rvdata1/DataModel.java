package id.ac.telkomuniversity.dph3a4.rvdata1;

class DataModel {
    String id;
    String namaPengadaan;
    String tglAwal;
    String tglAkhir;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNamaPengadaan() {
        return namaPengadaan;
    }

    public void setNamaPengadaan(String namaPengadaan) {
        this.namaPengadaan = namaPengadaan;
    }

    public String getTglAwal() {
        return tglAwal;
    }

    public void setTglAwal(String tglAwal) {
        this.tglAwal = tglAwal;
    }

    public String getTglAkhir() {
        return tglAkhir;
    }

    public void setTglAkhir(String tglAkhir) {
        this.tglAkhir = tglAkhir;
    }
}
